class Product {
  final String name;
  final String type;
  final String color;
  final String size;
  final String price;
  final int rating;
  final int ratingcount;
  final String image;
  final int discount;
  final DateTime dateAdded;
  final int quantity;

  Product({
    required this.name,
    required this.type,
    required this.color,
    required this.size,
    required this.price,
    required this.rating,
    required this.ratingcount,
    required this.image,
    required this.discount,
    required this.dateAdded,
    required this.quantity,
  });
}
